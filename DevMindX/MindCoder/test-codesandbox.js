// Quick test script for CodeSandbox integration
// Run with: node test-codesandbox.js

const API_URL = 'http://localhost:5000';

// You'll need to replace this with a valid token from your app
const TOKEN = 'YOUR_AUTH_TOKEN_HERE';

async function testCodeSandbox() {
  console.log('🧪 Testing CodeSandbox Integration...\n');

  // Test 1: Create a simple HTML sandbox
  console.log('Test 1: Creating a simple HTML sandbox...');
  try {
    const response = await fetch(`${API_URL}/api/preview/create`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${TOKEN}`
      },
      body: JSON.stringify({
        files: {
          'index.html': `
<!DOCTYPE html>
<html>
<head>
  <title>DevMindX Test</title>
  <style>
    body {
      font-family: Arial, sans-serif;
      display: flex;
      justify-content: center;
      align-items: center;
      height: 100vh;
      margin: 0;
      background: linear-gradient(135deg, #667eea 0%, #764ba2 100%);
      color: white;
    }
    .container {
      text-align: center;
      padding: 2rem;
      background: rgba(255, 255, 255, 0.1);
      border-radius: 20px;
      backdrop-filter: blur(10px);
    }
    h1 {
      font-size: 3rem;
      margin: 0;
      animation: pulse 2s infinite;
    }
    @keyframes pulse {
      0%, 100% { transform: scale(1); }
      50% { transform: scale(1.05); }
    }
  </style>
</head>
<body>
  <div class="container">
    <h1>🎉 CodeSandbox Works!</h1>
    <p>Generated by DevMindX</p>
    <p id="time"></p>
  </div>
  <script>
    setInterval(() => {
      document.getElementById('time').textContent = new Date().toLocaleTimeString();
    }, 1000);
  </script>
</body>
</html>
          `.trim()
        },
        template: 'static',
        title: 'DevMindX CodeSandbox Test',
        description: 'Testing CodeSandbox integration'
      })
    });

    if (!response.ok) {
      const error = await response.json();
      console.error('❌ Failed:', error.message);
      return;
    }

    const data = await response.json();
    console.log('✅ Success!');
    console.log('   Sandbox ID:', data.sandboxId);
    console.log('   Preview URL:', data.previewUrl);
    console.log('   Editor URL:', data.editorUrl);
    console.log('\n📱 Open the preview URL in your browser to see it live!\n');

  } catch (error) {
    console.error('❌ Error:', error.message);
  }

  // Test 2: Create a React sandbox
  console.log('\nTest 2: Creating a React sandbox...');
  try {
    const response = await fetch(`${API_URL}/api/preview/create`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${TOKEN}`
      },
      body: JSON.stringify({
        files: {
          'src/App.jsx': `
import React, { useState } from 'react';

export default function App() {
  const [count, setCount] = useState(0);

  return (
    <div style={{
      display: 'flex',
      flexDirection: 'column',
      alignItems: 'center',
      justifyContent: 'center',
      height: '100vh',
      fontFamily: 'Arial, sans-serif',
      background: 'linear-gradient(135deg, #667eea 0%, #764ba2 100%)',
      color: 'white'
    }}>
      <h1>🚀 React + CodeSandbox</h1>
      <p style={{ fontSize: '2rem' }}>Count: {count}</p>
      <button
        onClick={() => setCount(count + 1)}
        style={{
          padding: '1rem 2rem',
          fontSize: '1.2rem',
          background: 'white',
          color: '#667eea',
          border: 'none',
          borderRadius: '10px',
          cursor: 'pointer',
          fontWeight: 'bold'
        }}
      >
        Increment
      </button>
      <p style={{ marginTop: '2rem', opacity: 0.8 }}>
        Generated by DevMindX AI
      </p>
    </div>
  );
}
          `.trim(),
          'src/index.jsx': `
import React from 'react';
import ReactDOM from 'react-dom/client';
import App from './App';

const root = ReactDOM.createRoot(document.getElementById('root'));
root.render(<App />);
          `.trim(),
          'public/index.html': `
<!DOCTYPE html>
<html>
<head>
  <title>React App</title>
</head>
<body>
  <div id="root"></div>
</body>
</html>
          `.trim()
        },
        template: 'react',
        title: 'DevMindX React Test',
        description: 'Testing React with CodeSandbox'
      })
    });

    if (!response.ok) {
      const error = await response.json();
      console.error('❌ Failed:', error.message);
      return;
    }

    const data = await response.json();
    console.log('✅ Success!');
    console.log('   Sandbox ID:', data.sandboxId);
    console.log('   Preview URL:', data.previewUrl);
    console.log('   Editor URL:', data.editorUrl);
    console.log('\n📱 Open the preview URL in your browser to see the React app!\n');

  } catch (error) {
    console.error('❌ Error:', error.message);
  }
}

